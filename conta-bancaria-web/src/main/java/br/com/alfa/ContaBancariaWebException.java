package br.com.alfa;

public class ContaBancariaWebException extends Exception {

	private static final long serialVersionUID = -405395977697215815L;

	public ContaBancariaWebException() {
		super();
	}

	public ContaBancariaWebException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ContaBancariaWebException(String arg0) {
		super(arg0);
	}

	public ContaBancariaWebException(Throwable arg0) {
		super(arg0);
	}
	
	

}
